package CastTest;

public class CastTest {
    public static void main(String[] args) {

        System.out.println((int)'A' + (int)'A'); // char to int
        System.out.println(25 + (double)25.7);
        System.out.println(100 + (float)33.33F);
        System.out.println((int)27.44 + 1);

    }
}
